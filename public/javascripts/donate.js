// Function to donate to the contract
async function donate(amountInWei) {
  if (!web3) {
    console.error('Please connect MetaMask first.');
    return;
  }

  const donationStatusEl = document.getElementById('donationStatus');
  try {
    donationStatusEl.textContent = 'Processing donation...';

    const accounts = await web3.eth.getAccounts();
    const sender = accounts[0];
    const options = {
      from: sender,
      value: amountInWei.toString(),
    };

    await donationContract.methods.donate().send(options);

    donationStatusEl.textContent = 'Donation successful! Go to your metamask and import your free tooken using this address- "0xa81d05157b9e8ced37eed2cb759efde76c27e990"';
  } catch (error) {
    console.error(error);
    donationStatusEl.textContent = 'please wait...';
  } finally {
    // Optional: Reset donation amount or loading indicator
  }
}

// Bind donation form submission event (assuming you have a form with id="donationForm")
const donationForm = document.getElementById('donationForm');
donationForm.addEventListener('submit', event => {
  event.preventDefault();
  const amountInWei = web3.utils.toWei(donationForm.donationAmount.value, 'ether');
  donate(amountInWei);
});